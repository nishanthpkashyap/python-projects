class Number:
    def __init__(self):
        self.__two = 2
        self.__five = 5
        self.__ten = 10
        self.__two_hundred = 200

    def get_two(self):
        return self.__two

    def get_five(self):
        return self.__five

    def get_ten(self):
        return self.__ten

    def get_max_power(self):
        return self.__two_hundred
